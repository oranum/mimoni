import { Schema, model, models } from "mongoose"

const UserSchema = new Schema({
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ["user", "admin"],
        default: "user",
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
});

const User = models.User || model("User", UserSchema); // avoid recompiling the model if it already exists. models is the collection of all models, model is the function to create a new model

export default User;
